#Realizar un programa que permita el ingreso de dos números. Realizar la suma de los mismos
#Mostrar el resultado por pantalla con el siguiente formato: 
#"La suma de los dos números es: _____"

#int numeros enteros, float numeros con coma

numero_uno = int(input("Ingrese un número: "))
numero_dos = int(input("Ingrese otro número: "))

resultado = numero_uno + numero_dos

print(f"La suma de los dos numeros es: {resultado}")
